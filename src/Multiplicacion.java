
public class Multiplicacion {

	public static void mult(int num1, int num2) {
		int res = num1 * num2;
		System.out.println("La multiplicación es: " + res);
		
	}
	
	public static void multDouble(double num1, double num2) {
		double res = num1 * num2;
		System.out.println("La multiplicación es: " + res);
	}
}
